# Integer
age = 25
print(type(age))

# Floating-point
salary = 50000.5

# String
name = "John Doe"

# Boolean
is_male = True
is_married = False

# List
grads = [80, 95, 72,68, 95]

# Tuple
coordinates = (10.5, 20.5)

# Set
colors = {"red", "green", "blue"}

# Dictionary
person_info = {"name": "John Doe", "age": 25, "salary": 50000.5}

print("age:", age, type(age))
print("salary:", salary, type(salary))
print("name:", name, type(name))
print("is_male:", is_male, type(is_male))   
print("is_married:", is_married, type(is_married))
print("grads:", grads, type(grads))
print("coordinates:", coordinates, type(coordinates))
print("colors:", colors, type(colors))  
print("person_info:", person_info, type(person_info))
